package classes;

public class Departamento {
	
	private int codigo;
	private String nome;
	private String localizacao;
	private int codigoFuncionarioGerente;
	
	public int getCodigo() {
		return codigo;
	}
	public void setCodigo(int codigo) {
		this.codigo = codigo;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getLocalizacao() {
		return localizacao;
	}
	public void setLocalizacao(String localizacao) {
		this.localizacao = localizacao;
	}
	public int getCodigoFuncionarioGerente() {
		return codigoFuncionarioGerente;
	}
	public void setCodigoFuncionarioGerente(int codigoFuncionarioGerente) {
		this.codigoFuncionarioGerente = codigoFuncionarioGerente;
	}
	
}
